#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], sizeof(char));
		i++;
	}
}

int	main(int argc, char **argv)
{
	ft_putstr(argv[0]);
	write(1, "\n", 1);
	return (0);
}
